public class OperasiPerbandingan {
    public static void main(String[] args) {
        int value1 = 100;
        int value2 = 50;
        String StringValue = "10";

        System.out.println(value1 > value2); // lebih dari 
        System.out.println(value1 < value2); // kurang dari
        System.out.println(value1 >= value2); // lebih dari sama dengan
        System.out.println(value1 <= value2); // kurang dari sama dengan
        System.out.println(value1 == value2); //sama dengan
        System.out.println(value1 != value2); // tidak sama dengan
    }
}